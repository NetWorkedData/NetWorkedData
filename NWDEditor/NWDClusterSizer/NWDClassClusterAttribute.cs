namespace NetWorkedData
{
    //+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
    [AttributeUsage(AttributeTargets.Class, AllowMultiple = false)]
    public class NWDClassClusterAttribute : Attribute
    {
        //-------------------------------------------------------------------------------------------------------------
        public int Min;
        public int Max;
        //-------------------------------------------------------------------------------------------------------------
        public NWDClassClusterAttribute(int sMin, int sMax)
        {
            Min = sMin;
            Max = sMax;
        }
        //-------------------------------------------------------------------------------------------------------------
    }
    //+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
}
