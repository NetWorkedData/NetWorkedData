using NetWorkedData;
using UnityEngine;
using UnityEngine.UI;

//=====================================================================================================================
namespace SpiritOfBottle
{
    //+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
    /// <summary>
    /// 
    /// </summary>
    public class Quest : MonoBehaviour
    {
        //-------------------------------------------------------------------------------------------------------------
        public Text TextQuestName;
        //-------------------------------------------------------------------------------------------------------------
        // Quest delegate
        public delegate void questBlock(NWDQuest sQuest);
        public questBlock questBlockDelegate;
        //-------------------------------------------------------------------------------------------------------------
        private NWDQuest CurrentQuest;
        //-------------------------------------------------------------------------------------------------------------
        public void SetQuest(NWDQuest sQuest)
        {
            CurrentQuest = sQuest;
            TextQuestName.text = sQuest.Title.GetLocalString();
        }
        //-------------------------------------------------------------------------------------------------------------
        public void ShowQuestDialogue()
        {
            if (questBlockDelegate != null)
            {
                questBlockDelegate(CurrentQuest);
            }
        }
        //-------------------------------------------------------------------------------------------------------------
    }
}
