using UnityEngine;
using UnityEngine.UI;

//=====================================================================================================================
namespace NetWorkedData
{
    //+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
    /// <summary>
    /// Autolocalized text with an NWDLocalizationConnection object
    /// </summary>
    public class NWDAutolocalized : MonoBehaviour
    {
        //-------------------------------------------------------------------------------------------------------------
        public NWDLocalizationConnection ObjConnection;
        //-------------------------------------------------------------------------------------------------------------
        void Start()
        {
            NWDLocalization tObj = ObjConnection.GetObject();
            if (tObj != null)
            {
                string tText = tObj.GetLocalString();
                GetComponent<Text>().text = tText;
            }
        }
        //-------------------------------------------------------------------------------------------------------------
    }
    //+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
}
