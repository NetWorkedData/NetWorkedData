namespace NetWorkedData
{
    //+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
    static public partial class NWDData
    {
        //-------------------------------------------------------------------------------------------------------------
        /// <summary>
        /// Sync all Datas on server in active environment.
        /// </summary>
        public static void SyncAllDatas()
        {
            NWDDataManager.SharedInstance().AddWebRequestAllSynchronization();
        }
        //-------------------------------------------------------------------------------------------------------------
        /// <summary>
        ///  Sync Datas of sTypeList on server in active environment.
        /// </summary>
        /// <param name="sTypeList"></param>
        public static void SyncDatas(List<Type> sTypeList)
        {
            NWDDataManager.SharedInstance().AddWebRequestSynchronization(sTypeList);
        }
        //-------------------------------------------------------------------------------------------------------------
    }
