
using System;
using System.Collections;
using System.Collections.Generic;
using UnityEngine;
//=====================================================================================================================
namespace NetWorkedData
{
    //+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
    public class NWEClipboard : MonoBehaviour
    {
        //-------------------------------------------------------------------------------------------------------------
        public static void CopyToClipboard(string sString)
        {
            TextEditor tTextEditor = new TextEditor();
            tTextEditor.text = sString;
            tTextEditor.SelectAll();
            tTextEditor.Copy();
        }
        //-------------------------------------------------------------------------------------------------------------
    }
    //+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
}
//=====================================================================================================================
