
using System;
using UnityEngine;
//=====================================================================================================================
namespace NetWorkedData
{
    //+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
    public class NWDDebug
    {
        //-------------------------------------------------------------------------------------------------------------
        public static bool Active = true;
        //-------------------------------------------------------------------------------------------------------------
        public static void Log(string sString)
        {
            if (Active == true)
            {
                Debug.Log(sString);
            }
        }
        //-------------------------------------------------------------------------------------------------------------
        public static void Warning(string sString)
        {
            if (Active == true)
            {
                Debug.LogWarning(sString);
            }
        }
        //-------------------------------------------------------------------------------------------------------------
    }
    //+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
}
//=====================================================================================================================
